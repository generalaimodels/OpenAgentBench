from __future__ import annotations

from dataclasses import dataclass

_API_OVERVIEW_URL = "https://developers.openai.com/api/reference/overview"


@dataclass(slots=True, frozen=True)
class OpenAIEndpointSpec:
    name: str
    path: str
    python_sdk_call: str
    input_modalities: tuple[str, ...]
    output_modalities: tuple[str, ...]
    supports_mixed_content: bool
    notes: str
    source_url: str
    category: str = "platform"
    lifecycle: str = "stable"
    documented_operations: tuple[str, ...] = ()
    documented_subresources: tuple[str, ...] = ()


def _endpoint(
    *,
    name: str,
    path: str,
    python_sdk_call: str,
    input_modalities: tuple[str, ...],
    output_modalities: tuple[str, ...],
    supports_mixed_content: bool,
    notes: str,
    source_url: str = _API_OVERVIEW_URL,
    category: str = "platform",
    lifecycle: str = "stable",
    documented_operations: tuple[str, ...] = (),
    documented_subresources: tuple[str, ...] = (),
) -> OpenAIEndpointSpec:
    return OpenAIEndpointSpec(
        name=name,
        path=path,
        python_sdk_call=python_sdk_call,
        input_modalities=input_modalities,
        output_modalities=output_modalities,
        supports_mixed_content=supports_mixed_content,
        notes=notes,
        source_url=source_url,
        category=category,
        lifecycle=lifecycle,
        documented_operations=documented_operations,
        documented_subresources=documented_subresources,
    )


