def endpoints_for_modality(modality: str) -> list[OpenAIEndpointSpec]:
    normalized = modality.strip().lower()
    return [
        endpoint
        for endpoint in OPENAI_ENDPOINTS
        if normalized in endpoint.input_modalities or normalized in endpoint.output_modalities
    ]


def endpoints_for_category(category: str) -> list[OpenAIEndpointSpec]:
    normalized = category.strip().lower()
    return [endpoint for endpoint in OPENAI_ENDPOINTS if endpoint.category == normalized]


def endpoints_for_lifecycle(lifecycle: str) -> list[OpenAIEndpointSpec]:
    normalized = lifecycle.strip().lower()
    return [endpoint for endpoint in OPENAI_ENDPOINTS if endpoint.lifecycle == normalized]


